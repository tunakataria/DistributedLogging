package com.example.extservices;

public class LoadPlan {
    public LoadPlan(String name, String numberOfPeople, String numberOfCargo, String totalWeight) {
        this.name = name;
        this.numberOfPeople = numberOfPeople;
        this.numberOfCargo = numberOfCargo;
        this.totalWeight = totalWeight;
    }

    private String name ;

    private String numberOfPeople;

    private String numberOfCargo;

    private String totalWeight;

}
